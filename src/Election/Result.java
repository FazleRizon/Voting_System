    private void GetResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GetResultActionPerformed
        // TODO add your handling code here:
        String Elect = Elections.getSelectedItem().toString();
        String winnerName="",winnerVote="";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/votingsystem", "root","");
            String Quary = "SELECT  sum(Vote) FROM `candidate` WHERE  election_name = '"+Elect+"' ORDER by vote DESC;";
            PreparedStatement pst = con.prepareStatement(Quary);
            ResultSet res = pst.executeQuery();
            res.next();
            String total = res.getString(1);
            Quary = "SELECT `candidate_ID`, `Name`, `Vote` FROM `candidate` WHERE  election_name = '"+Elect+"' ORDER by vote DESC;";
            pst = con.prepareStatement(Quary);
             res = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) VoterTable.getModel();
            model.setRowCount(0);
            
            if(res.next()){
                MainLb.setText("Congratulations to "+res.getString(2)+"");
                winnerName = res.getString(2);
                winnerVote =  res.getString(3);
                Double percent = (Double.parseDouble(winnerVote)/Double.parseDouble(total))*100.0;
                subLb.setText(winnerName+" has won the election with "+percent+"% vote");
                String [] datarow = new String[]{res.getString(1), res.getString(2), res.getString(3),String.format("%.2f", percent)+"%"};
                model.addRow(datarow);
                while(res.next()){
                    percent = (Double.parseDouble(res.getString(3))/Double.parseDouble(total))*100.0;
                datarow = new String[]{res.getString(1), res.getString(2), res.getString(3),String.format("%.2f", percent)+"%"};
                model.addRow(datarow);
                }
                
            }
            
            
            
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }

    }//GEN-LAST:event_GetResultActionPerformed

