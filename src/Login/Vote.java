    public Vote(String user, String pass){
        initComponents();
        setTitle("Vote");
        Toolkit toolkit = getToolkit();
        Dimension size = toolkit.getScreenSize();
        setLocation(size.width / 2 - getWidth() / 2, size.height / 2 - getHeight() / 2);
        usernameme=user;
        password= pass;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/votingsystem", "root","");
            String quary = "SELECT `election` FROM `voterinfo` \n" +
                   
                    "WHERE username = '"+user+"' and pass = '"+pass+"'";
            PreparedStatement pst = con.prepareStatement(quary);
            ResultSet res = pst.executeQuery();
            if(res.next()){
                ElectionTitle.setText(res.getString(1));
                electionName = res.getString(1);
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "voter data not found in database");
            }
            quary = "SELECT `candidate_ID`, `Name`,  `symbol_name`, `symbol_path` FROM `candidate` \n" +
                    "WHERE election_name = \""+electionName+"\" ;";
            pst = con.prepareStatement(quary);
            res = pst.executeQuery();
            Candidates.removeAllItems();
            candidatelist = new int[15];
            
            int count=0;
            while(res.next()){
                Candidates.addItem(res.getString(2)+" - "+res.getString(3));
                candidatelist[count++]=res.getInt(1);
            }
            
            
           
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

