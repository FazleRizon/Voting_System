    private void voteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_voteBtnActionPerformed
        // TODO add your handling code here:
        if(count!=1){
            warning.setText("You have already Voted");
        }
        else if( selectedCndidID==0){
            warning.setText("Please choose your preferred candidate");
        }
        else{
            warning.setText("");
            try{
                 Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/votingsystem", "root","");
            String quary = "SELECT `candidate_ID`,  `Vote` FROM `candidate` WHERE candidate_ID = "+selectedCndidID+";";
            PreparedStatement pst = con.prepareStatement(quary);
            ResultSet res = pst.executeQuery();
            if(res.next()){
                int preVote = res.getInt(2);
                   preVote++;
                   quary = "UPDATE `candidate` SET `Vote`= "+preVote+" WHERE candidate_ID = "+selectedCndidID+";";
                   pst = con.prepareStatement(quary);
                   int c1 = pst.executeUpdate();
                   quary = "update  `users` set isEnbaled=0 WHERE `username` = '"+usernameme+"' and `pass` = '"+password+"';;";
                   pst = con.prepareStatement(quary);
                   int c2 = pst.executeUpdate();
                   if(c1==1 && c2==1){
                       JOptionPane.showMessageDialog(rootPane, "You have sucessfully Voted your preferred candidate");
                   }
                   else{
                       JOptionPane.showMessageDialog(rootPane, "An Error Occured");
                   }

               }
               con.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
        
    }//GEN-LAST:event_voteBtnActionPerformed

